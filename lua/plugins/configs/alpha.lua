-- footer
dash.section.footer.val = function()
    -- plugins count
    local function count(folder)
        return #vim.fn.globpath(plugins_path .. "/" .. folder, "*", 0, 1)
    end
    local counter = count("start") + count("opt")
    -- startup time
    local file = utils.read_lines(startup_path)
    local time = (not vim.tbl_isempty(file) and vim.split(file[#file], " ")[1] or nil)
    local version = ver.major .. "." .. ver.minor .. "." .. ver.patch
    return ""
        .. version
        .. " loaded "
        .. counter
        .. " plugin" .. (counter > 1 and "s" or "")
        .. (time and " in " .. time .. "ms" or "")
        .. "\n               Good Luck."
end

